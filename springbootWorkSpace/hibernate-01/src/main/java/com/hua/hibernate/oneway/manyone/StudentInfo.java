package com.hua.hibernate.oneway.manyone;

/**
 * 组件映射 学生组件
 */
public class StudentInfo {

    public int age;
    public String sex;

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }
}
